package advent.year2019.day20

import org.assertj.core.api.Assertions.assertThat
import org.junit.jupiter.api.Test

class DonutMazeTest {

  @Test
  fun `stepsToComplete -- first example -- 23`() {
    val input = """
         A           
         A           
  #######.#########  
  #######.........#  
  #######.#######.#  
  #######.#######.#  
  #######.#######.#  
  #####  B    ###.#  
BC...##  C    ###.#  
  ##.##       ###.#  
  ##...DE  F  ###.#  
  #####    G  ###.#  
  #########.#####.#  
DE..#######...###.#  
  #.#########.###.#  
FG..#########.....#  
  ###########.#####  
             Z       
             Z       
    """.trimIndent()

    val maze = DonutMaze.parse(input)

    val steps = maze.stepsToComplete()

    assertThat(steps).isEqualTo(23)
  }

